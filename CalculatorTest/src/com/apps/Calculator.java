package com.apps;

public class Calculator {
    /**
     *
     * @param dividend
     * @param divisor
     * @return Quotient
     */
    public int integerDivision(final int dividend, final int divisor) {
        return dividend / divisor;
    }
    /**
     *
     * @param minuend
     * @param subtrahend
     * @return result
     */
    public int integerSubtraction(final int minuend, final int subtrahend) {
        return minuend - subtrahend;
    }
}
